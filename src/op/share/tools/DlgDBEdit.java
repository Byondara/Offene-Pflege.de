    private void loadTable() {
        tblDB.setModel(new TMResultSet(rs));
        tblDB.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
        for (int i = 0; i < tblDB.getColumnCount(); i++) {
            tblDB.getColumnModel().getColumn(i).setCellRenderer(new RNDStandard());
        }
    }

    private void reloadResultSet() {
        try {
            PreparedStatement stmt = OPDE.getDb().db.prepareStatement(sql);
            rs = stmt.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            table = rsmd.getTableName(1);
        } catch (SQLException ex) {
            new DlgException(ex);
        }
    }

