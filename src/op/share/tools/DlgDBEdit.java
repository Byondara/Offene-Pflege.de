    private void tblDBMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDBMousePressed
        if (evt.isPopupTrigger()) {
            Point p = evt.getPoint();
            ListSelectionModel lsm = tblDB.getSelectionModel();
            final int row = tblDB.rowAtPoint(p);
            lsm.setSelectionInterval(row, row);
            SYSTools.unregisterListeners(menu);
            menu = new JPopupMenu();
            /**
             * Neueingabe
             */
            boolean neuMöglich = true;
            // Neueingabe
            JMenuItem itemPopupNew = new JMenuItem("Neu");
            itemPopupNew.addActionListener(new java.awt.event.ActionListener() {

                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    new DlgRecordEdit(parent, rs, -1, "Neueingabe");
                    reloadResultSet();
                    loadTable();
                }
            });
            menu.add(itemPopupNew);
            itemPopupNew.setEnabled(neuMöglich);
            /**
             * KORRIGIEREN
             */
            boolean bearbeitenMöglich = !lsm.isSelectionEmpty();
            // KORRIGIEREN
            JMenuItem itemPopupEdit = new JMenuItem("Korrigieren");
            itemPopupEdit.addActionListener(new java.awt.event.ActionListener() {

                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    new DlgRecordEdit(parent, rs, row, "Korrigieren von Datensatz aus '" + titel + "'");
                    reloadResultSet();
                    loadTable();
                }
            });
            menu.add(itemPopupEdit);
            itemPopupEdit.setEnabled(bearbeitenMöglich);
            menu.show(evt.getComponent(), (int) p.getX(), (int) p.getY());
        }
    }//GEN-LAST:event_tblDBMousePressed

