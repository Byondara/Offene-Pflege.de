    private void jspVorlagenComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jspVorlagenComponentResized
        JScrollPane jsp = (JScrollPane) evt.getComponent();
        Dimension dim = jsp.getSize();
        // Größe der Text Spalten im DFN ändern.
        // Summe der fixen Spalten  = 175 + ein bisschen
        int textWidth = dim.width - (80 + 80 + 25);
        TableColumnModel tcm1 = tblVorlagen.getColumnModel();
        if (tcm1.getColumnCount() < 4) {
            return;
        }

        tcm1.getColumn(TMVorlagen.COL_STICHWORT).setPreferredWidth(textWidth / 2);
        tcm1.getColumn(TMVorlagen.COL_BWNAME).setPreferredWidth(textWidth / 2);
        tcm1.getColumn(TMVorlagen.COL_VON).setPreferredWidth(80);
        tcm1.getColumn(TMVorlagen.COL_BIS).setPreferredWidth(80);

        tcm1.getColumn(TMVorlagen.COL_STICHWORT).setHeaderValue("Stichwort");
        tcm1.getColumn(TMVorlagen.COL_BWNAME).setHeaderValue("BewohnerIn");
        tcm1.getColumn(TMVorlagen.COL_VON).setHeaderValue("Von");
        tcm1.getColumn(TMVorlagen.COL_BIS).setHeaderValue("Bis");
    }//GEN-LAST:event_jspVorlagenComponentResized

