    private void reloadTable() {
        if (txtSuche.getText().equals("")) {
            tblVorlagen.setModel(new DefaultTableModel());
            return;
        }
        ListSelectionModel lsm = tblVorlagen.getSelectionModel();
        if (lsl != null) {
            lsm.removeListSelectionListener(lsl);
        }
        lsl = new HandleSelections();

        tblVorlagen.setModel(new TMVorlagen(txtSuche.getText(), cbAbgesetzte.isSelected()));
        lsm.addListSelectionListener(lsl);
        tblVorlagen.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
        jspVorlagen.dispatchEvent(new ComponentEvent(jspVorlagen, ComponentEvent.COMPONENT_RESIZED));
        tblVorlagen.getColumnModel().getColumn(TMVorlagen.COL_STICHWORT).setCellRenderer(new RNDStandard());
        tblVorlagen.getColumnModel().getColumn(TMVorlagen.COL_BWNAME).setCellRenderer(new RNDStandard());
        tblVorlagen.getColumnModel().getColumn(TMVorlagen.COL_VON).setCellRenderer(new RNDStandard());
        tblVorlagen.getColumnModel().getColumn(TMVorlagen.COL_BIS).setCellRenderer(new RNDStandard());
    }

