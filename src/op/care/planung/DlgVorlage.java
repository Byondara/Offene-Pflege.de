    class HandleSelections
            implements ListSelectionListener {

        public void valueChanged(ListSelectionEvent lse) {
            TableModel tm = tblVorlagen.getModel();
            if (tm.getRowCount() <= 0) {
                return;
            }

            if (!lse.getValueIsAdjusting()) {
                DefaultListSelectionModel lsm = (DefaultListSelectionModel) lse.getSource();
                if (lsm.isSelectionEmpty()) {
                    btnOK.setEnabled(false);
                    planid = 0;
                } else {
                    planid = ((Long) tm.getValueAt(lsm.getLeadSelectionIndex(), TMVorlagen.COL_PLANID)).longValue();
                    btnOK.setEnabled(true);
                }
                //if (ts.)
            }

        }
    }
